# Дан целочисленный список A размера N (< 15). Переписать в новый целочисленный список B все элементы с порядковыми номерами, кратными трем (3, 6, ...), и вывести размер полученного списка B и его содержимое. Условный оператор не использовать.

import random

try:
    n = int(input('Введите размер списка A (N < 15): '))
    if n >= 15:
        print('Введенное число должно быть меньше 15.')
        n = 0
except ValueError:
    print('Ошибка: введено не число.')
    n = 0

if n > 0:
    A = [random.randint(1, 50) for _ in range(n)]
    print('Исходный список A:', A)

    B = []
    for k in range(3, n + 1, 3):
        B.append(A[k - 1])

    print('Размер списка B:', len(B))
    print('Содержимое списка B:', B)
else:
    print('Не удалось сформировать список из-за неправильного ввода.')