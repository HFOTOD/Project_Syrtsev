# Дан список A размера N. Найти максимальный элемент из его элементов с нечетными номерами: A₁, A₃, A₅, ...

import random

n = int(input('Введите размер списка: '))
A = [random.randint(1, 100) for _ in range(n)]

print('Исходный список:', A)

max_val = A[1]  # первый элемент с нечётным номером (номер 2 → индекс 1)
for i in range(3, n + 1, 2):  # перебираем нечётные *номера*: 3, 5, 7...
    if A[i - 1] > max_val:
        max_val = A[i - 1]

print('Максимальный элемент с нечётным номером:', max_val)
