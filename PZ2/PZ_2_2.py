#Даны числа x, y. Проверить истинность высказывания: «Точка с координатами (x, y) лежит в первой или третьей координатной четверти».

def check_quadrant(x, y):
    """
    Проверяет, лежит ли точка (x, y) в первой или третьей четверти.
    Первая четверть: x > 0 и y > 0
    Третья четверть: x < 0 и y < 0
    """
    return (x > 0 and y > 0) or (x < 0 and y < 0)

print("Проверка четверти точки")

while True:
    try:
        x = float(input("Введите координату x: "))
        y = float(input("Введите координату y: "))
        break
    except ValueError:
        print("Ошибка: введите корректное число для координат.")

result = check_quadrant(x, y)
print(f"Точка ({x}, {y}) лежит в первой или третьей четверти: {result}")

#Дано целое число в диапазоне 1–7. Вывести строку — название дня недели, соответствующее данному числу (1 — «понедельник», 2 — «вторник»)

def get_day_name(day_number):
    """
    Возвращает название дня недели по номеру (1-7)
    """
    days = {
        1: "понедельник",
        2: "вторник",
        3: "среда",
        4: "четверг",
        5: "пятница",
        6: "суббота",
        7: "воскресенье"
    }
    return days.get(day_number, "Некорректный номер дня")

print("Название дня недели")
try:
    day_num = int(input("Введите число от 1 до 7: "))
    if 1 <= day_num <= 7:
        day_name = get_day_name(day_num)
        print(f"День недели: {day_name}")
    else:
        print("Ошибка: число должно быть в диапазоне от 1 до 7.")
except ValueError:
    print("Ошибка: введите целое число.")
