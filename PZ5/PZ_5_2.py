# Описать функцию AddLeftDigit(D, K), добавляющую к целому положительному числу K слева цифру D (D — входной параметр целого типа, лежащий в диапазоне 1-9, K — параметр целого типа, являющийся одновременно входным и выходным). С помощью этой функции последовательно добавить к данному числу K слева данные цифры D1 и D2, выводя результат каждого добавления.

def AddLeftDigit(D, K):
    if not (1 <= D <= 9):
        raise ValueError("Цифра D должна быть от 1 до 9")
    if K < 0:
        raise ValueError("Число K должно быть неотрицательным")
    return int(str(D) + str(K))

K = input("Введите целое неотрицательное число K: ")
while True:
    try:
        K = int(K)
        if K < 0:
            print("Число K должно быть неотрицательным!")
            K = input("Введите целое неотрицательное число K: ")
        else:
            break
    except ValueError:
        print("Неверный ввод! Введите целое число.")
        K = input("Введите целое неотрицательное число K: ")

D1 = input("Введите первую цифру D1 (от 1 до 9): ")
while True:
    try:
        D1 = int(D1)
        if 1 <= D1 <= 9:
            break
        else:
            print("Цифра D1 должна быть от 1 до 9!")
            D1 = input("Введите первую цифру D1 (от 1 до 9): ")
    except ValueError:
        print("Неверный ввод! Введите цифру от 1 до 9.")
        D1 = input("Введите первую цифру D1 (от 1 до 9): ")

K = AddLeftDigit(D1, K)
print(f"После добавления D1={D1} слева: {K}")

D2 = input("Введите вторую цифру D2 (от 1 до 9): ")
while True:
    try:
        D2 = int(D2)
        if 1 <= D2 <= 9:
            break
        else:
            print("Цифра D2 должна быть от 1 до 9!")
            D2 = input("Введите вторую цифру D2 (от 1 до 9): ")
    except ValueError:
        print("Неверный ввод! Введите цифру от 1 до 9.")
        D2 = input("Введите вторую цифру D2 (от 1 до 9): ")

K = AddLeftDigit(D2, K)
print(f"После добавления D2={D2} слева: {K}")
